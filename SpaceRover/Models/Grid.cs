namespace SpaceRover.Models;

/// <summary>
/// Represents a grid with a specified width and height.
/// </summary>
public class Grid
{
    /// <summary>
    /// Gets or sets the width of the grid.
    /// </summary>
    public double Width { get; init; }
    
    /// <summary>
    /// Gets or sets the height of the grid.
    /// </summary>
    public double Height { get; init; }
}